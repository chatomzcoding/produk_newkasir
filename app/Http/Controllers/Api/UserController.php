    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if (!cektoken($request->token)) {
            return response()->json('akses terlarang');
        }
        $namafile   = uploadgambar($request,'user');
        User::create([
            'name' => $request->name,
            'email' => $request->email,
            'level' => $request->level,
            'password' => Hash::make($request->password),
            'profile_photo_path' => $namafile,
        ]);

        return response()->json([
            'success' => 1,
            'message' => 'success'
        ]);
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show($user)
    {
        $token = $_GET['token'];
        if (cektoken($token)) {
            $result = DB::table('users')
                        ->join('user_akses','users.id','=','user_akses.user_id')
                        ->join('client','user_akses.client_id','=','client.id')
                        ->select('client.*','users.*')
                        ->where('users.id',$user)
                        ->first();
            return $result;
        } else {
            return response()->json('akses dilarang');
        }
    }

